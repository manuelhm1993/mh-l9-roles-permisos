     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // Obtener el archivo
        $file = $request->file('file');

        // Obtener el nombre del archivo, guardarlo en la carpeta pÃºblica y obtener su ruta
        $name = $file->getClientOriginalName();
        $path = 'storage/' . $file->store('files', 'public');

        // Crear un nuevo modelo File
        $file = File::create([
            'name' => $name,
            'path' => $path,
        ]);

        // Redirigir al usuario a files.index con un feedback
        return to_route('files.index')->with('status', 'File has been uploaded successfully in Laravel');
    }

